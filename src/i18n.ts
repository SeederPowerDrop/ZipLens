export type LanguageCode = keyof typeof translations;

let currentLang: LanguageCode = 'en';

export function setLanguage(lang: LanguageCode) {
    if (translations[lang]) {
        currentLang = lang;
        document.documentElement.lang = lang;
        if (lang === 'ar') {
            document.documentElement.dir = 'rtl';
        } else {
            document.documentElement.dir = 'ltr';
        }
        updateDOM();
    }
}

export function getTranslation(key: keyof typeof translations['en']): string {
    return translations[currentLang][key] || translations['en'][key];
}

export function getCurrentLang() {
    return currentLang;
}

function updateDOM() {
    // Update elements that have static i18n
    const el = (id: string, text: string) => {
        const node = document.getElementById(id);
        if (node) {
            // Check if we need to keep SVGs
            const svg = node.querySelector('svg');
            if (svg) {
                // Remove all text nodes, keep SVG, append new text
                Array.from(node.childNodes).forEach(child => {
                    if (child.nodeType === Node.TEXT_NODE) {
                        node.removeChild(child);
                    }
                });
                node.appendChild(document.createTextNode(' ' + text));
            } else {
                node.innerText = text;
            }
        }
    };

    const elHtml = (id: string, html: string) => {
        const node = document.getElementById(id);
        if (node) node.innerHTML = html;
    };
    
    const elAttr = (id: string, attr: string, val: string) => {
        const node = document.getElementById(id);
        if (node) node.setAttribute(attr, val);
    };

    const elClassText = (className: string, text: string) => {
        const nodes = document.querySelectorAll(`.${className}`);
        nodes.forEach(node => {
            node.textContent = text;
        });
    }

    elClassText('titlebar-title', getTranslation('titlebar'));
    elClassText('drop-text', getTranslation('dropText'));
    elClassText('drop-subtext', getTranslation('dropSubtext'));
    
    el('btn-extract', getTranslation('btnExtract'));
    el('btn-compress-file', getTranslation('btnCompressFile'));
    el('btn-compress', getTranslation('btnCompressFolder'));
    
    elAttr('split-select', 'placeholder', getTranslation('splitPlaceholder'));
    
    const setPwdSpan = document.querySelector('.toggle-text');
    if (setPwdSpan) setPwdSpan.textContent = getTranslation('setPassword');
    
    elAttr('compress-password', 'placeholder', getTranslation('passwordPlaceholder'));
    
    // Update select options (partial for aes/zipcrypto)
    const encryptLevel = document.getElementById('encrypt-level') as HTMLSelectElement;
    if (encryptLevel) {
        if (encryptLevel.options[0]) encryptLevel.options[0].text = getTranslation('aes256');
        if (encryptLevel.options[1]) encryptLevel.options[1].text = getTranslation('zipCrypto');
    }

    el('select-all', getTranslation('selectAll'));
    el('deselect-all', getTranslation('deselectAll'));
    el('toggle-all', getTranslation('invertSelection'));
    
    elAttr('preview-search', 'placeholder', getTranslation('searchPlaceholder'));
    el('btn-reveal', getTranslation('revealInFinder'));

    elHtml('password-modal-title', getTranslation('passwordRequired'));
    elHtml('password-modal-desc', getTranslation('passwordDesc'));
    el('modal-error-msg', getTranslation('passwordIncorrect'));
    elAttr('unlock-password', 'placeholder', getTranslation('unlockPlaceholder'));
    el('password-cancel', getTranslation('cancel'));
    el('password-submit', getTranslation('unlock'));

    el('conflict-modal-title', getTranslation('fileConflict'));
    el('conflict-overwrite', getTranslation('conflictOverwrite'));
    el('conflict-keep', getTranslation('conflictKeep'));
    el('conflict-cancel', getTranslation('conflictCancel'));

    el('extract-error-title', getTranslation('corruptedFile'));
    el('extract-err-ignore-all', getTranslation('ignoreAll'));
    el('extract-err-ignore', getTranslation('ignore'));
    el('extract-err-cancel', getTranslation('abort'));

    el('report-title-default', getTranslation('extractionReport')); // Handled dynamically in main.ts too
    el('report-failed-title', getTranslation('failedFiles'));
    el('report-success-title', getTranslation('successFiles'));
    
    el('report-export-txt', getTranslation('exportTxt'));
    el('report-export-csv', getTranslation('exportCsv'));
    el('report-close', getTranslation('close'));
}
